package com.exerciseOne;

public class Main {
    public static void main(String[] args) {
        // 8 bits
        byte n1 = 3;
        // 16 bits
        short n2 = 5;
        // 32 bits
        int n3 = 7;
        // 64 bits
        long n4 = 11;

        //32 bits
        float d1 = 13.5f;
        // 64 bits
        double d2 = 15.5d;

        // 1 bits
        boolean b1 = true;

        String txt1 = "Hello";
        // 16 bits
        char txt2 = 'c';

        System.out.println("byte: " + n1);
        System.out.println("short: " + n2);
        System.out.println("int: " + n3);
        System.out.println("long: " + n4);

        System.out.println("float: " + d1);
        System.out.println("double: " + d2);

        System.out.println("boolean: " + b1);

        System.out.println("String: " + txt1);
        System.out.println("char: " + txt2);
    }
}
